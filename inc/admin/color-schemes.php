function pagespeed_get_color_scheme_choices() {
	GLOBAL $page_speed_color_schemes;
	$choices = array();
	foreach ( $page_speed_color_schemes as $key => $value ) {
		$choices[ $key ] = array(
			'url'   => HELIUM_ADMIN_IMAGES_URI . '/color-schemes/' . $key . '.png',
			'label' => $key
		);
	}
	return $choices;
}
